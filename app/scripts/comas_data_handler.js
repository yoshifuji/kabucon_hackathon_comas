monster = function(data)
{
  //  会社名
  this.name = data.name;
  // 株価
  this.attack = data.attack;
  //  時価総額
  this.defence = data.defence;
  //  出来高
  this.speed = data.speed;
  //  特色
  this.attribute = data.attribute;
  //  データベース上の連番
  this.id = data.id;
  //  売買代金
  this.life = data.life;
  //  従業員数
  this.dextality = data.dextality;
  //  平均年齢
  this.luck = data.luck;
  //  企業ロゴアイコン
  this.icon = data.icon;
};



